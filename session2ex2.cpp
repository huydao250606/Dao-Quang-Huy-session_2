#include <stdio.h>

int main() {
    // Khai bao va khoi tao hai bien so nguyen
    int a = 5; // Bien so nguyen thu nhat
    int b = 10; // Bien so nguyen thu hai

    // Tinh tong cua hai bien va luu ket qua vao bien khac
    int tong = a + b; // Bien luu ket qua tong

    // In ra ket qua
    printf("Tong cua %d va %d la: %d\n", a, b, tong);

    return 0;
}
